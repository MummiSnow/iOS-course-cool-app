import UIKit

func add (valueA: Double, valueB: Double) -> Double{
    return valueA + valueB
}

add(100, valueB: 200)

func subtract (valueA: Int, valueB: Int) -> Int{
    return valueA - valueB
}

subtract(100, valueB: 20)

func multiply(valueA: Float, valueB: Float) -> Float {
    return valueA * valueB
}

multiply(2, valueB: 8)

func divide (valueA: Double, valueB: Double) -> Double {
    return valueA / valueB
}

divide(1, valueB: 2)
